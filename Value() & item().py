# value()
my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}
values = my_dict.values()
print("Values in the dictionary:", values)

# output: Values in the dictionary: dict_values(['John', 25, 'New York'])


# item()
# Example dictionary

my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}
items = my_dict.items()
print("Items in the dictionary:", items)
#OutPut:  Items in the dictionary: dict_items([('name', 'John'), ('age', 25), ('city', 'New York')])



